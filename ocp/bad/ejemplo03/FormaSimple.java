public class FormaSimple {
    private String tipo;
    private double a;
    private double b;

    public FormaSimple(String tipo, double a, double b) {
        this.tipo = tipo;
        this.a = a;
        this.b = b;
    }

    public String getTipo() {
        return tipo;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
